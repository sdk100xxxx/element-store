/** @type {import('next').NextConfig} */
const path = require('path');
const fs = require('fs');

// Load .env from project root so Stripe keys are always found (fixes cwd issues)
const envPath = path.join(__dirname, '.env');
if (fs.existsSync(envPath)) {
  const content = fs.readFileSync(envPath, 'utf8');
  content.split('\n').forEach((line) => {
    const trimmed = line.trim();
    if (trimmed && !trimmed.startsWith('#')) {
      const eq = trimmed.indexOf('=');
      if (eq > 0) {
        const key = trimmed.slice(0, eq).trim();
        let val = trimmed.slice(eq + 1).trim();
        if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
          val = val.slice(1, -1);
        }
        process.env[key] = val;
      }
    }
  });
}

// Ensure NEXTAUTH_URL is set at build time (avoids "Invalid URL" during prerender)
if (!process.env.NEXTAUTH_URL?.trim()) {
  process.env.NEXTAUTH_URL = "https://placeholder.vercel.app";
}

